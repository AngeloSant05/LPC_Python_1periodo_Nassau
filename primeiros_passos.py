#Um breve resumo de como funciona o Git e o GitHub.
#O Git é um versionador de arquivos, aí você me pergunta, Ângelo o que é isso?
#O nome já é sugestivo, mas um versionador de arquivos serve para podermos 
#modificar arquivos e salvar cada uma dessas modificações, podendo tanto salvar,
#quanto retroceder o processo caso você cometa erros, algo que é bem normal de 
#acontecer.

#OBS: O Git é um versionador LOCAL, o que significa que tudo que você alterar
#na SUA MÁQUINA, vai ficar na SUA MÁQUINA. Vamos ver um pouco disso mais pra frente.

#O GitHub é similar ao Git, porém funciona de uma forma um pouco diferente.
#O GitHub funciona de forma remota, tudo que você faz no Git pode ser mandado
